<p style="color:#0080FF; font-size:24px;">Tên Công ty:<b> CÔNG TY TNHH TEE&TEE VIỆT NAM</b></p>
<p>Địa chỉ: <b>Hà Nội</b></p>
<br/>


<p><div style="font-weight:bold; font-size:25px">1- Giới thiệu chung</div></p>
<br/>
<p>Được thành lập năm 2011, Tee&Tee được biết đến như là một thương hiệu uy tín chuyên sản xuất và phân phối các sản phẩm công nghệ cao cấp như máy tính xách tay, laptop, chuột, bàn phím, loa...</p>
<br/>
<p>Với xưởng sản xuất gần 100 công nhân thợ lành nghề và những nhà thiết kế sản phẩm chuyên nghiệp, các mẫu mã mang thương hiệu Tee&Tee được cho ra liên tục để bắt kịp xu hướng công nghệ.
Hệ thống phân phối gồm chuỗi 28 cửa hàng độc quyền trên toàn quốc đảm bảo chất lượng hàng hóa chính hãng Tee&Tee với mức giá tốt nhất mà không qua bất kỳ khâu trung gian nào.</p>
<br/>
<p>Thương hiệu Tee&Tee được khách hàng yêu mến vì sự đơn giản, chắc chắn và chất lượng, chấm phá thêm nét cổ điển trong từng sản phẩm cùng đội ngũ nhân viên thân thiện, tận tình sẽ giúp khách hàng có những sự lựa chọn sản phẩm phù hợp nhất cho mình. </p>
<br/>
<p>Với chính sách đổi trả và bảo hành trong suốt quá trình sử dụng, quý khách hàng có thể yên tâm mua sắm những chiếc máy tính, sản phẩm mình yêu thích để làm bạn đồng hành hoặc làm quà tặng cho người thân mà không lo lắng về hư hại hay có những lỗi nhỏ khi sử dụng.</p>
<br/>
<p>Tee&Tee sẽ không ngừng nỗ lực để nâng cao chất lượng sản phẩm và dịch vụ, đáp ứng sự yêu mến cũng như kỳ vọng của quý khách hàng.</p>
